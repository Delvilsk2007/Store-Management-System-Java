    private void loademp() {
        try {
            // 1️⃣ Connect to MySQL
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/project", "root", "stcgs@12");
            Statement stmt = con.createStatement();

            // 2️⃣ Disable foreign key checks to avoid constraint error
            stmt.executeUpdate("SET FOREIGN_KEY_CHECKS = 0;");

            // 3️⃣ Reset and renumber emp table
            stmt.executeUpdate("SET @count = 0;");
            stmt.executeUpdate("UPDATE emp SET S_NO = @count := @count + 1;");
            stmt.executeUpdate("ALTER TABLE emp AUTO_INCREMENT = 1;");

            // 4️⃣ Reset and renumber emp_personal table
            stmt.executeUpdate("SET @count = 0;");
            stmt.executeUpdate("UPDATE emp_personal SET S_NO = @count := @count + 1;");
            stmt.executeUpdate("ALTER TABLE emp_personal AUTO_INCREMENT = 1;");

            // 5️⃣ Re-enable foreign key checks
            stmt.executeUpdate("SET FOREIGN_KEY_CHECKS = 1;");

            // 6️⃣ Load Employee (Official) Details into jTable1
            ResultSet rs1 = stmt.executeQuery("SELECT * FROM emp ORDER BY S_NO ASC");
            DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();
            model1.setRowCount(0); // clear old rows

            while (rs1.next()) {
                model1.addRow(new Object[]{
                    rs1.getInt("S_NO"),
                    rs1.getString("Emp_Name"),
                    rs1.getString("Gender"),
                    rs1.getString("Status"),
                    rs1.getDouble("Salary")
                });
            }

            // 7️⃣ Load Employee (Personal) Details into jTable2
            ResultSet rs2 = stmt.executeQuery("SELECT * FROM emp_personal ORDER BY S_NO ASC");
            DefaultTableModel model2 = (DefaultTableModel) jTable2.getModel();
            model2.setRowCount(0); // clear old rows

            while (rs2.next()) {
                model2.addRow(new Object[]{
                    rs2.getInt("S_NO"),
                    rs2.getString("Address"),
                    rs2.getString("Mobile_No"),
                    rs2.getString("Email")
                });
            }

            // 8️⃣ Close connection and show confirmation
            con.close();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "⚠️ Error: " + e.getMessage());
        }
    }

