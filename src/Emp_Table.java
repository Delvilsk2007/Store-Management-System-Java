    private void Edit_Uset_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Edit_Uset_btnActionPerformed
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select an employee first.");
            return;
        }

        int sno = Integer.parseInt(jTable1.getValueAt(row, 0).toString());

        try (Connection con = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/project", "root", "stcgs@12"); Statement stmt = con.createStatement()) {

            // Load emp data
            ResultSet rs = stmt.executeQuery("SELECT * FROM emp WHERE S_no=" + sno);
            if (!rs.next()) {
                JOptionPane.showMessageDialog(this, "Employee record not found.");
                rs.close();
                return;
            }

            String name = rs.getString("Emp_Name");
            String gender = rs.getString("Gender");
            String status = rs.getString("Status");
            double salary = rs.getDouble("Salary");
            rs.close();

            // Load personal data
            rs = stmt.executeQuery("SELECT * FROM emp_personal WHERE S_no=" + sno);
            String address = "";
            String phone = "";
            String email = "";
            if (rs.next()) {
                address = rs.getString("Address");
                phone = rs.getString("Mobile_No");
                email = rs.getString("Email");     // UPDATED
            }
            rs.close();

            // -----------------------
            // 1) UPDATE NAME
            // -----------------------
            String newName = JOptionPane.showInputDialog(this, "Enter Name:", name);
            if (newName != null && !newName.trim().isEmpty()
                    && !newName.trim().equals(name == null ? "" : name.trim())) {

                try (PreparedStatement pst = con.prepareStatement(
                        "UPDATE emp SET Emp_Name=? WHERE S_no=?")) {

                    pst.setString(1, newName.trim());
                    pst.setInt(2, sno);
                    pst.executeUpdate();
                }
            }

            // -----------------------
            // 2) UPDATE GENDER
            // -----------------------
            String[] genders = {"Male", "Female", "Other"};
            JComboBox<String> cbGender = new JComboBox<>(genders);

            String genderNorm = (gender == null) ? "" : gender.trim();
            boolean matched = false;

            for (String g : genders) {
                if (g.equalsIgnoreCase(genderNorm)) {
                    cbGender.setSelectedItem(g);
                    matched = true;
                    break;
                }
            }

            if (!matched && !genderNorm.isEmpty()) {
                cbGender.insertItemAt(genderNorm, 0);
                cbGender.setSelectedIndex(0);
            }

            int optG = JOptionPane.showConfirmDialog(
                    this,
                    new Object[]{"Select Gender:", cbGender},
                    "Gender",
                    JOptionPane.OK_CANCEL_OPTION
            );

            if (optG == JOptionPane.OK_OPTION) {
                String selectedGender = cbGender.getSelectedItem().toString().trim();
                if (!selectedGender.equalsIgnoreCase(genderNorm)) {
                    try (PreparedStatement pst = con.prepareStatement(
                            "UPDATE emp SET Gender=? WHERE S_no=?")) {

                        pst.setString(1, selectedGender);
                        pst.setInt(2, sno);
                        pst.executeUpdate();
                    }
                }
            }

            // -----------------------
            // 3) UPDATE STATUS
            // -----------------------
            String[] stats = {"Active", "Inactive"};
            JComboBox<String> cbStatus = new JComboBox<>(stats);

            if (status != null) {
                for (String s : stats) {
                    if (s.equalsIgnoreCase(status.trim())) {
                        cbStatus.setSelectedItem(s);
                        break;
                    }
                }
            }

            int optS = JOptionPane.showConfirmDialog(
                    this,
                    new Object[]{"Select Status:", cbStatus},
                    "Status",
                    JOptionPane.OK_CANCEL_OPTION
            );

            if (optS == JOptionPane.OK_OPTION) {
                String sel = cbStatus.getSelectedItem().toString().trim();
                if (!sel.equalsIgnoreCase(status == null ? "" : status.trim())) {

                    try (PreparedStatement pst = con.prepareStatement(
                            "UPDATE emp SET Status=? WHERE S_no=?")) {

                        pst.setString(1, sel);
                        pst.setInt(2, sno);
                        pst.executeUpdate();
                    }
                }
            }

            // -----------------------
            // 4) UPDATE SALARY
            // -----------------------
            String newSal = JOptionPane.showInputDialog(this, "Enter Salary:", salary);
            if (newSal != null && !newSal.trim().isEmpty()) {
                double salVal = Double.parseDouble(newSal.trim());

                if (salVal != salary) {
                    try (PreparedStatement pst = con.prepareStatement(
                            "UPDATE emp SET Salary=? WHERE S_no=?")) {

                        pst.setDouble(1, salVal);
                        pst.setInt(2, sno);
                        pst.executeUpdate();
                    }
                }
            }

            // -----------------------
            // 5) UPDATE ADDRESS
            // -----------------------
            String newAdd = JOptionPane.showInputDialog(this, "Enter Address:", address);
            if (newAdd != null && !newAdd.trim().isEmpty()
                    && !newAdd.trim().equals(address == null ? "" : address.trim())) {

                try (PreparedStatement pst = con.prepareStatement(
                        "UPDATE emp_personal SET Address=? WHERE S_no=?")) {

                    pst.setString(1, newAdd.trim());
                    pst.setInt(2, sno);
                    pst.executeUpdate();
                }
            }

            // -----------------------
            // 6) UPDATE PHONE
            // -----------------------
            String newPhone = JOptionPane.showInputDialog(this, "Enter Phone:", phone);
            if (newPhone != null && !newPhone.trim().isEmpty()
                    && !newPhone.trim().equals(phone.trim())) {

                try (PreparedStatement pst = con.prepareStatement(
                        "UPDATE emp_personal SET Mobile_No=? WHERE S_no=?")) {

                    pst.setString(1, newPhone.trim());
                    pst.setInt(2, sno);
                    pst.executeUpdate();
                }
            }

            // -----------------------
            // 7) UPDATE EMAIL
            // -----------------------
            String newEmail = JOptionPane.showInputDialog(this, "Enter Email:", email);
            if (newEmail != null && !newEmail.trim().isEmpty()
                    && !newEmail.trim().equals(email == null ? "" : email.trim())) {

                try (PreparedStatement pst = con.prepareStatement(
                        "UPDATE emp_personal SET Email=? WHERE S_no=?")) {

                    pst.setString(1, newEmail.trim());
                    pst.setInt(2, sno);
                    pst.executeUpdate();
                }
            }

            loademp();
            JOptionPane.showMessageDialog(this, "Employee updated.");

        } catch (NumberFormatException nf) {
            JOptionPane.showMessageDialog(this, "Salary must be a valid number.");
            nf.printStackTrace();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }

    }//GEN-LAST:event_Edit_Uset_btnActionPerformed
