    private void Search_user_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Search_user_btnActionPerformed
        try {
    Class.forName("com.mysql.cj.jdbc.Driver");

    Connection con = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/project", "root", "stcgs@12");

    String[] options = {"Id_No.", "Name", "Status"};
    String choice = (String) JOptionPane.showInputDialog(
            this,
            "Search employee by:",
            "Search Option",
            JOptionPane.QUESTION_MESSAGE,
            null,
            options,
            options[0]
    );

    if (choice == null) {
        JOptionPane.showMessageDialog(this, "Search cancelled.");
        return;
    }

    String value = JOptionPane.showInputDialog(this, "Enter " + choice + ":");
    if (value == null || value.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "No " + choice + " entered.");
        return;
    }

    PreparedStatement pst;

    // Prepare main emp query
    if (choice.equals("Id_No.")) {
        pst = con.prepareStatement("SELECT * FROM emp WHERE S_no = ?");
        pst.setInt(1, Integer.parseInt(value.trim()));
    } else if (choice.equals("Name")) {
        pst = con.prepareStatement("SELECT * FROM emp WHERE Emp_Name = ?");
        pst.setString(1, value.trim());
    } else {
        pst = con.prepareStatement("SELECT * FROM emp WHERE Status = ?");
        pst.setString(1, value.trim());
    }

    ResultSet rs = pst.executeQuery();

    StringBuilder output = new StringBuilder();
    boolean found = false;

    // Clear both tables
    DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel(); // emp
    DefaultTableModel model2 = (DefaultTableModel) jTable2.getModel(); // emp_personal

    model1.setRowCount(0);
    model2.setRowCount(0);

    while (rs.next()) {

        found = true;

        int sno = rs.getInt("S_no");
        String empName = rs.getString("Emp_Name");
        String gender = rs.getString("Gender");
        String status = rs.getString("Status");
        double salary = rs.getDouble("Salary");

        // Fetch personal info
        PreparedStatement ps = con.prepareStatement("SELECT * FROM emp_personal WHERE S_no = ?");
        ps.setInt(1, sno);
        ResultSet rs2 = ps.executeQuery();

        String address = "", phone = "", email = "";

        if (rs2.next()) {
            address = rs2.getString("Address");
            phone = rs2.getString("Mobile_No");
            email = rs2.getString("Email");
        }

        // Text output
        output.append(
                "S_no       : " + sno + "\n"
                + "Name       : " + empName + "\n"
                + "Gender     : " + gender + "\n"
                + "Status     : " + status + "\n"
                + "Salary     : " + salary + "\n"
                + "--- Personal Info ---\n"
                + "Address    : " + address + "\n"
                + "Phone      : " + phone + "\n"
                + "Email      : " + email + "\n"
                + "------------------------------\n"
        );

        // ADD TO EMP TABLE (jTable1)
        model1.addRow(new Object[]{
            sno, empName, gender, status, salary
        });

        // ADD TO PERSONAL TABLE (jTable2)
        model2.addRow(new Object[]{
            sno, address, phone, email
        });

        rs2.close();
        ps.close();
    }

    if (!found) {
        JOptionPane.showMessageDialog(this, "No record found with the given " + choice + ".");
    } else {
        JOptionPane.showMessageDialog(this, output.toString());
    }

    rs.close();
    pst.close();
    con.close();

} catch (Exception e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
}

    }//GEN-LAST:event_Search_user_btnActionPerformed

